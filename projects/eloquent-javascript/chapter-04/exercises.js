// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
let rest = null;

for (let i = array.length - 1; i >= 0; i--){
  rest = { value: array[i], rest: rest};

}
return rest;

};

