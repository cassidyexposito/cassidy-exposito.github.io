// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
if(x < y){
  return x;
} else if(x > y){
  return y;
} else {
  return 0;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(n) {
  //base 
  if(n === 0){
    return true;
  // recursion
  } else if (n === 1 || n === -1){
    return false;
  } 
  
  return isEven(n - 2);
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  let count = 0;

  for(let i = 0; i < string.length; i++){
    if (string.charAt(i) === letter){
      count += 1
    }
    
  } return count;
};

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0;

  for (let i = 0; i < string.length; i++){
    if (string.charAt(i) === "B"){
      count +=1
    }
  }
  return count;
}

